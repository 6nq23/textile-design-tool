/**
 * svg-exporter.js — Export segmented/colorized design as SVG
 * Converts contour data + region colors into a scalable vector graphic.
 */

function exportSVG(segData, imgWidth, imgHeight) {
    if (!segData || !segData.contours || !segData.regions) {
        throw new Error('No segmentation data available for SVG export.');
    }

    const { contours, regions, regionMap, width, height } = segData;

    // Build a color lookup from region data
    const regionColors = {};
    regions.forEach(r => {
        if (r.color) regionColors[r.id] = r.color;
    });

    // SVG header
    let svg = `<?xml version="1.0" encoding="UTF-8"?>\n`;
    svg += `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}" width="${width}" height="${height}">\n`;
    svg += `  <title>Textile Design Export</title>\n`;
    svg += `  <desc>Generated by Textile Design Tool</desc>\n`;

    // Background
    svg += `  <rect width="${width}" height="${height}" fill="#ffffff"/>\n`;

    // Group for filled regions
    svg += `  <g id="regions">\n`;

    // For each region, find the contours that belong to it using regionMap + centroid
    regions.forEach(region => {
        const color = region.color || '#ffffff';
        const cx = Math.round(region.centroid.x);
        const cy = Math.round(region.centroid.y);

        // Find contours whose interior contains this region's centroid
        // We'll draw the region as a path from contour points
        let matchedContour = null;
        let bestArea = Infinity;

        for (const contour of contours) {
            if (contour.isHole) continue;
            if (contour.points.length < 3) continue;

            // Check if centroid is inside this contour using ray casting
            if (pointInPolygon(cx, cy, contour.points) && contour.area < bestArea) {
                matchedContour = contour;
                bestArea = contour.area;
            }
        }

        if (matchedContour && matchedContour.points.length >= 3) {
            const d = contourToSVGPath(matchedContour.points);
            svg += `    <path d="${d}" fill="${color}" stroke="#000000" stroke-width="1" opacity="0.9" data-region="${region.id}" data-label="${region.label}"/>\n`;
        } else {
            // Fallback: draw a filled rect from bounding box
            const bb = region.boundingBox;
            svg += `    <rect x="${bb.left}" y="${bb.top}" width="${bb.width}" height="${bb.height}" fill="${color}" stroke="#000000" stroke-width="0.5" opacity="0.85" data-region="${region.id}"/>\n`;
        }
    });

    svg += `  </g>\n`;

    // Group for edge/outline contours
    svg += `  <g id="outlines" fill="none" stroke="#000000" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round">\n`;

    contours.forEach((contour, i) => {
        if (contour.points.length < 2) return;
        if (contour.isHole) return; // skip holes for outline layer
        const d = contourToSVGPath(contour.points);
        svg += `    <path d="${d}"/>\n`;
    });

    svg += `  </g>\n`;
    svg += `</svg>`;

    return svg;
}

/**
 * Convert an array of {x,y} points to an SVG path "d" attribute.
 */
function contourToSVGPath(points) {
    if (points.length < 2) return '';
    let d = `M ${points[0].x} ${points[0].y}`;
    for (let i = 1; i < points.length; i++) {
        d += ` L ${points[i].x} ${points[i].y}`;
    }
    d += ' Z';
    return d;
}

/**
 * Ray-casting point-in-polygon test.
 */
function pointInPolygon(x, y, polygon) {
    let inside = false;
    for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
        const xi = polygon[i].x, yi = polygon[i].y;
        const xj = polygon[j].x, yj = polygon[j].y;

        const intersect = ((yi > y) !== (yj > y)) &&
            (x < (xj - xi) * (y - yi) / (yj - yi) + xi);
        if (intersect) inside = !inside;
    }
    return inside;
}

/**
 * Download an SVG string as a file.
 */
function downloadSVG(svgString, filename) {
    const blob = new Blob([svgString], { type: 'image/svg+xml' });
    const a = document.createElement('a');
    a.href = URL.createObjectURL(blob);
    a.download = filename || 'design.svg';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(a.href);
}
